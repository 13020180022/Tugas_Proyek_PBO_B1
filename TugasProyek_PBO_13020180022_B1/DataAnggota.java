    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("IdAnggota");
        model.addColumn("Nama");
        model.addColumn("Jurusan");
        model.addColumn("Telp");
        model.addColumn("Email");
        
        try{
            int No = 1;
            String sql = "SELECT * FROM DataAnggota where IdAnggota like '%"
                    + txtcari.getText() + "%'"
                    +"or Nama like '%" + txtcari.getText()
                    + "%'" + "or Jurusan like '%" + txtcari.getText()
                    + "%'" + "or Telp like '%" + txtcari.getText()
                    + "%'" + "or Email like '%" + txtcari.getText() + "%'";
            java.sql.Connection conn = (Connection)DatabasePerpustakaan.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            
            while(res.next()){
                model.addRow(new Object[]{No++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
                
            }
            tabelanggota.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }

