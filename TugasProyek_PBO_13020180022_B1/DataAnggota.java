    private void tabelanggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelanggotaMouseClicked
        // TODO add your handling code here:
        int baris = tabelanggota.rowAtPoint(evt.getPoint());
        
        String IdAnggota = tabelanggota.getValueAt(baris, 1).toString();
        txtIdAnggota.setText(IdAnggota);
        
        String Nama = tabelanggota.getValueAt(baris, 2).toString();
        txtNama.setText(Nama);
        
        String Jurusan = tabelanggota.getValueAt(baris, 3).toString();
        txtJurusan.setText(Jurusan);
        
        String Telp = tabelanggota.getValueAt(baris, 4).toString();
        txtTelp.setText(Telp);
        
        String Email = tabelanggota.getValueAt(baris, 5).toString();
        txtEmail.setText(Email);
    }//GEN-LAST:event_tabelanggotaMouseClicked

