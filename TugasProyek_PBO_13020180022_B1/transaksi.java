    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
        try{
            BufferedWriter simpan = new BufferedWriter (new FileWriter ("hasil.txt"));
            String panggil = "IdAnggota :\t\t"+outIdAnggota.getText()+"\n"+
                               "Nama :\t\t"+outNama.getText()+"\n"+
                               "TotalDenda :\t\t"+outTotalDenda.getText();
                        simpan.write(panggil);
                        JOptionPane.showMessageDialog(null, "Databerhasil disimpan !");
                        simpan.close();
            
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btsimpanActionPerformed

