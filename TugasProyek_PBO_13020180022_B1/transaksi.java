    private void bthitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bthitungActionPerformed
        // TODO add your handling code here:
        String IdAnggota = txtIdAnggota.getText();
        String Nama = txtNama.getText();
        if (txtIdAnggota.getText().isEmpty() || txtNama.getText().isEmpty()
                || txtJumlah.getText().isEmpty()
                || txtDenda.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null,"Form Masih Kosong!!");
        }
        else {
            Double TotalDenda = (Double.parsenDouble(txtDenda.getText())+Double.parsenDouble(txtJumlah.getText()));
            outTotalDenda.setText(TotalDenda.toString());
            outIdAnggota.setText(IdAnggota);
            outNama.setText(Nama);
        }
    }//GEN-LAST:event_bthitungActionPerformed

