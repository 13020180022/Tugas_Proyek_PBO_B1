    private void tabelpeminjamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpeminjamMouseClicked
        // TODO add your handling code here:
        int baris = tabelpeminjam.rowAtPoint(evt.getPoint());
        
        String IdAnggota = tabelpeminjam.getValueAt(baris, 1).toString();
        txtIdAnggota.setText(IdAnggota);
        
        String Nama= tabelpeminjam.getValueAt(baris, 2).toString();
        txtNama.setText(Nama);
        
        String KodeBuku = tabelpeminjam.getValueAt(baris, 3).toString();
        txtKodeBuku.setText(KodeBuku);
        
        String JudulBuku = tabelpeminjam.getValueAt(baris, 4).toString();
        txtJudulBuku.setText(JudulBuku);
        
        String TanggalPinjam = tabelpeminjam.getValueAt(baris, 5).toString();
        txtTanggalPinjam.setText(TanggalPinjam);
        
        String TanggalKembali = tabelpeminjam.getValueAt(baris, 5).toString();
        txtTanggalKembali.setText(TanggalKembali);
    }//GEN-LAST:event_tabelpeminjamMouseClicked

