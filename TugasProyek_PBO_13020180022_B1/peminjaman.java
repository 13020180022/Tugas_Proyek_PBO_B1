    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("IdAnggota");
        model.addColumn("Nama");
        model.addColumn("KodeBuku");
        model.addColumn("JudulBuku");
        model.addColumn("TanggalPinjam");
        model.addColumn("TanggalKembali");
        
        try{
            int No = 1;
            String sql = "SELECT * FROM peminjaman";
            java.sql.Connection conn = (Connection)DatabasePerpustakaan.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            
            while(res.next()){
                model.addRow(new Object[]{No++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
                
            }
            tabelpeminjam.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error : " + e.getMessage());
        }
    }

